    public void loadTable() {
        DefaultTableModel model = (DefaultTableModel) jTableAlunos.getModel();
        model.setNumRows(0);
        
        ArrayList<Produto> list = new ArrayList();
        
        list = product.getList();
        
        for (Produto p : list) {
            model.addRow(new Object[] {
                p.getProduct_id(),
                p.getName(),
                p.getDescription(),
                p.getStock(),
                p.getPrice(),
                p.getDate_registered(),
            });
       }
    }

